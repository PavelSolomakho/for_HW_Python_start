from fractions import Fraction


def add_fractions(f1, f2):
    """
    Функция принимает две строки с дробями и возвращает их сумму в виде строки.
    """
    # Извлекаем числитель и знаменатель первой дроби
    n1, d1 = map(int, f1.split('/'))

    # Извлекаем числитель и знаменатель второй дроби
    n2, d2 = map(int, f2.split('/'))

    # Вычисляем сумму дробей
    result = Fraction(n1, d1) + Fraction(n2, d2)

    # Возвращаем результат в виде строки
    return str(result)


def multiply_fractions(f1, f2):
    """
    Функция принимает две строки с дробями и возвращает их произведение в виде строки.
    """
    # Извлекаем числитель и знаменатель первой дроби
    n1, d1 = map(int, f1.split('/'))

    # Извлекаем числитель и знаменатель второй дроби
    n2, d2 = map(int, f2.split('/'))

    # Вычисляем произведение дробей
    result = Fraction(n1, d1) * Fraction(n2, d2)

    # Возвращаем результат в виде строки
    return str(result)


# Запрос дробей у пользователя
f1 = input("Введите первую дробь в формате a/b: ")
f2 = input("Введите вторую дробь в формате a/b: ")

# Сложение дробей
sum_result = add_fractions(f1, f2)
print(f"Результат сложения дробей {f1} и {f2}: {sum_result}")

# Умножение дробей
mult_result = multiply_fractions(f1, f2)
print(f"Результат умножения дробей {f1} и {f2}: {mult_result}")
